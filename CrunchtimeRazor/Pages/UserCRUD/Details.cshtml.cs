using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using Crunchtime.Entities;
using CrunchtimeRazor.Data;

namespace CrunchtimeRazor.Pages.UserCRUD
{
    public class DetailsModel : PageModel
    {
        private readonly CrunchtimeRazor.Data.ApplicationDbContext _context;

        public DetailsModel(CrunchtimeRazor.Data.ApplicationDbContext context)
        {
            _context = context;
        }

        public User User { get; set; } = default!;

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var user = await _context.User.FirstOrDefaultAsync(m => m.Userid == id);
            if (user == null)
            {
                return NotFound();
            }
            else
            {
                User = user;
            }
            return Page();
        }
    }
}
